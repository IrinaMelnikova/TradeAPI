package APIRequest;

public class Rates {

    private Double high;
    private Double vol;
    private Double avg;
    private Double vol_cur;
    private Double last;
    private Double low;
    private Double buy;
    private Double sell;
    private Double updated;

    public Double getHigh() {
        return high;
    }
    public Double getVol() {
        return vol;
    }
    public Double getAvg() {
        return avg;
    }
    public Double getVol_cur() {
        return vol_cur;
    }
    public Double getLast() {
        return last;
    }
    public Double getLow() {
        return low;
    }
    public Double getBuy() {
        return buy;
    }
    public Double getSell() {
        return sell;
    }
    public Double getUpdated() {
        return updated;
    }
}
